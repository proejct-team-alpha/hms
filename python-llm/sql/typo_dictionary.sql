-- 오타 교정 사전 테이블
CREATE TABLE IF NOT EXISTS typo_dictionary (
    id INT AUTO_INCREMENT PRIMARY KEY,
    typo VARCHAR(50) NOT NULL UNIQUE COMMENT '오타 표기',
    correct_term VARCHAR(50) NOT NULL COMMENT '올바른 표기',
    category VARCHAR(30) DEFAULT '' COMMENT '카테고리 (body_part, disease, department, test 등)',
    hit_count INT DEFAULT 0 COMMENT '교정 사용 횟수',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_category (category)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

